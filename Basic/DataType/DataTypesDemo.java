package Basic;
public class DataTypesDemo {
    public static void main(String[] args) {
        // Integer types
        byte byteVar = 100; // 8-bit
        short shortVar = 1000; // 16-bit
        int intVar = 100000; // 32-bit
        long longVar = 10000000000L; // 64-bit

        // Floating point types
        float floatVar = 10.5f; // 32-bit
        double doubleVar = 99.99; // 64-bit

        // Character type
        char charVar = 'A'; // 16-bit Unicode character

        // Boolean type
        boolean boolVar = true;

        // Printing out the variables
        System.out.println("Byte Value: " + byteVar);
        System.out.println("Short Value: " + shortVar);
        System.out.println("Int Value: " + intVar);
        System.out.println("Long Value: " + longVar);
        System.out.println("Float Value: " + floatVar);
        System.out.println("Double Value: " + doubleVar);
        System.out.println("Char Value: " + charVar);
        System.out.println("Boolean Value: " + boolVar);
    }
}
